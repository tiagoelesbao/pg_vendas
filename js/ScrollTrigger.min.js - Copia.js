// Selecione as imagens que deseja animar
const images = gsap.utils.toArray('.panel img');

// Percorra todas as imagens e crie uma animação para cada uma
images.forEach((image) => {
  gsap.fromTo(
    image,
    { x: '100%' }, // posição inicial
    {
      x: '0%', // posição final
      ease: 'none',
      scrollTrigger: {
        trigger: image.parentElement,
        start: 'top bottom', // início da animação
        end: 'bottom top', // fim da animação
        scrub: true // suavizar a animação enquanto o usuário rola
      }
    }
  );
});